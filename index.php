<script>
  function log_auth(){
    let MyData = Object.fromEntries(new FormData(document.forms['log_in']));
   

    fetch(`BackEnd/Controller.php`, {
    method: 'POST',
    body: JSON.stringify(MyData)
  })
  .then(response => response.json())
  .then(data => {


SessionExport.setSession(data);

if(data['login_status']==true){
  location.href='dashboard.php'
}

if(SessionExport.Session()){
  location.href='dashboard.php'
}
  })
  }

  window.onload = ()=>{
    if(SessionExport.Session()){
  location.href='dashboard.php'
}
  }
